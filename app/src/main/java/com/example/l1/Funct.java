package com.example.l1;

class Funct {
    
    public int mMin(int a, int b) {
        if (a < b){
            return a;
        }
        else {
            return b;
        }
    }

    public int mMax(int a, int b) {
        if (a > b) {
            return a;
        }
        else {
            return b;
        }
    }
}
